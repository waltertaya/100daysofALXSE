package main

import "fmt"

func main() {
	// strings variable declaration
	var name string = "Walter Taya"
	var name1 = "Walter Taya"
	name2 := "Walter Taya"
	var name3 string
	name3 = "Walter Taya"
	fmt.Println(name)
	fmt.Println(name1)
	fmt.Println(name2)
	fmt.Println(name3)
	// Variables in Go : string, int, float64, bool
	var age int = 25
	var age1 = 25
	age2 := 25
	var age3 int
	age3 = 25
	fmt.Println(age)
	fmt.Println(age1)
	fmt.Println(age2)
	fmt.Println(age3)
	// int8, int16, int32, int64
	// int8 : -128 to 127
	// int16 : -32768 to 32767
	// int32 : -2147483648 to 2147483647
	// int64 : -9223372036854775808 to 9223372036854775807
	// uint8, uint16, uint32, uint64 (unsigned integers)
	// uint8 : 0 to 255
	// uint16 : 0 to 65535
	// uint32 : 0 to 4294967295
	// uint64 : 0 to 18446744073709551615
	// float32, float64
	// float32 : 1.18e-38 to 3.4e+38
	// float64 : 2.23e-308 to 1.80e+308
}
